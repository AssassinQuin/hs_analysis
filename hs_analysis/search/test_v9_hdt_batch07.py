# Test 7: Taunt dies → index shift → new target
# ===================================================================

def test_07_taunt_minion_dies_opponent_new_target():
    """Kill taunt with first minion → second minion can now target vulnerable minion.

    Uses two attackers: first kills taunt, then second can target any opponent minion.
    """
    state = HDTGameStateFactory.create_state(
        turn=5,
        player_mana=5,
        player_board=[
            {"name": "攻击者A", "tags": {"ATK": 4, "HEALTH": 4, "EXHAUSTED": 0}},
            {"name": "攻击者B", "tags": {"ATK": 2, "HEALTH": 2, "EXHAUSTED": 0}},
        ],
        opponent_hp=30,
        opponent_armor=0,
        opponent_board=[
            {"name": "嘲讽", "tags": {"ATK": 1, "HEALTH": 2, "TAUNT": 1}},
            {"name": "脆皮", "tags": {"ATK": 5, "HEALTH": 1}},
        ],
    )

    # Verify taunt blocking: only taunt minion (index 1) is a legal target
    legal = enumerate_legal_actions(state)
    attack_actions = [a for a in legal if a.action_type == "ATTACK"]
    targets = {a.target_index for a in attack_actions}
    assert targets == {1}, (
        f"With taunt present, only target_index=1 should be legal, got {targets}"
    )

    # Step 1: 攻击者A (index 0) attacks taunt (target_index=1) — 4 damage kills 1/2 taunt
    attack1 = Action(action_type="ATTACK", source_index=0, target_index=1)
    state = apply_action(state, attack1)

    # Taunt should be dead, only 脆皮 remains
    assert len(state.opponent.board) == 1, (
        f"After killing taunt, opponent should have 1 minion, got {len(state.opponent.board)}"
    )
    assert state.opponent.board[0].name == "脆皮", (
        "Remaining opponent minion should be 脆皮"
    )
    assert not state.opponent.board[0].has_taunt, "脆皮 should not have taunt"

    # Step 2: 攻击者B (index 1 in original board → now index 0 after death cleanup)
    # can now attack face OR 脆皮 since taunt is dead
    legal2 = enumerate_legal_actions(state)
    attack_actions2 = [a for a in legal2 if a.action_type == "ATTACK"]
    targets2 = {a.target_index for a in attack_actions2}
    assert 0 in targets2, "After taunt dies, face (target_index=0) should be attackable"
    assert 1 in targets2, "After taunt dies, 脆皮 (target_index=1) should be attackable"


