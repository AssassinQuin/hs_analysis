# Test 1: Lethal with weapon + minion
# ===================================================================

def test_01_lethal_with_weapon_and_minion():
    """Weapon (3/2) + minion (4/4 can_attack) should yield >= 7 damage bound."""
    state = HDTGameStateFactory.create_state(
        turn=5,
        player_mana=5,
        player_weapon={"name": "战刃", "attack": 3, "durability": 2},
        player_board=[
            {"name": "猛击随从", "tags": {"ATK": 4, "HEALTH": 4, "EXHAUSTED": 0}},
        ],
        opponent_hp=7,
        opponent_armor=0,
        opponent_board=[],
    )

    # max_damage_bound should count weapon (3) + minion (4) = 7
    bound = max_damage_bound(state)
    assert bound >= 7, f"max_damage_bound should be >= 7 (3 weapon + 4 minion), got {bound}"

    # check_lethal: weapon attacks (source_index=-1) are generated by
    # _enumerate_damage_actions but NOT by enumerate_legal_actions, so
    # the DFS legality check rejects them. This is a known limitation —
    # lethal checker finds minion-only lethal fine but not weapon-inclusive paths.
    # Verify with minion-only lethal (opponent at 4 HP instead of 7).
    state_low = HDTGameStateFactory.create_state(
        turn=5,
        player_mana=5,
        player_board=[
            {"name": "猛击随从", "tags": {"ATK": 4, "HEALTH": 4, "EXHAUSTED": 0}},
        ],
        opponent_hp=4,
        opponent_armor=0,
        opponent_board=[],
    )
    lethal_minion_only = check_lethal(state_low, time_budget_ms=20.0)
    assert lethal_minion_only is not None, "check_lethal should find minion-only lethal"


