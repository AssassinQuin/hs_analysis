# Test 5: Multiple death cleanup
# ===================================================================

def test_05_multiple_death_cleanup():
    """Mutual kill in combat: 3/1 attacks 2/3 → both die. Then 2/1 attacks face."""
    state = HDTGameStateFactory.create_state(
        turn=5,
        player_mana=5,
        player_board=[
            {"name": "攻方A", "tags": {"ATK": 3, "HEALTH": 1, "EXHAUSTED": 0}},
            {"name": "攻方B", "tags": {"ATK": 2, "HEALTH": 1, "EXHAUSTED": 0}},
        ],
        opponent_hp=30,
        opponent_armor=0,
        opponent_board=[
            {"name": "守方", "tags": {"ATK": 2, "HEALTH": 3}},
        ],
    )

    # Step 1: 攻方A (3/1) attacks 守方 (2/3, index 1 in opponent board → target_index=1)
    # 3/1 deals 3 → 2/3 becomes 2/0 → dead
    # 2/3 counter-deals 2 → 3/1 becomes 3/-1 → dead
    attack1 = Action(action_type="ATTACK", source_index=0, target_index=1)
    state = apply_action(state, attack1)

    # After mutual kill: player has 1 minion (攻方B), opponent has 0 minions
    assert len(state.board) == 1, (
        f"Player should have 1 minion after mutual kill, got {len(state.board)}"
    )
    assert state.board[0].name == "攻方B", (
        f"Surviving player minion should be 攻方B, got '{state.board[0].name}'"
    )
    assert len(state.opponent.board) == 0, (
        f"Opponent should have 0 minions after defender dies, got {len(state.opponent.board)}"
    )

    # Step 2: 攻方B (2/1, now at index 0) attacks face (target_index=0)
    opponent_hp_before = state.opponent.hero.hp
    attack2 = Action(action_type="ATTACK", source_index=0, target_index=0)
    state = apply_action(state, attack2)

    assert state.opponent.hero.hp == opponent_hp_before - 2, (
        f"Opponent HP should be {opponent_hp_before - 2} after 2/1 face attack, "
        f"got {state.opponent.hero.hp}"
    )


