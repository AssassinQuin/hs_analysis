def test_06_mana_exactly_zero_after_plays():
    """Turn 4 (4 mana), play two 2-cost cards → mana exactly 0."""
    state = HDTGameStateFactory.create_state(
        turn=4,
        player_mana=4,
        player_hand=[
            {
                "name": "二费A",
                "type": "MINION",
                "tags": {"COST": 2, "ATK": 2, "HEALTH": 3},
            },
            {
                "name": "二费B",
                "type": "MINION",
                "tags": {"COST": 2, "ATK": 2, "HEALTH": 3},
            },
        ],
    )

    assert state.mana.available == 4, f"Initial mana should be 4, got {state.mana.available}"

    # Play first 2-cost card (card_index=0)
    play1 = Action(action_type="PLAY", card_index=0, position=0)
    state = apply_action(state, play1)
    assert state.mana.available == 2, (
        f"After first play, mana should be 2, got {state.mana.available}"
    )

    # After first play, hand shifted — card_index=0 is now the second card
    play2 = Action(action_type="PLAY", card_index=0, position=1)
    state = apply_action(state, play2)
    assert state.mana.available == 0, (
        f"After second play, mana should be 0, got {state.mana.available}"
    )


