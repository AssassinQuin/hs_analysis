def test_08_engine_with_all_zero_hp_opponent():
    """Engine should handle already-dead opponent gracefully (no crash)."""
    state = HDTGameStateFactory.create_state(
        turn=6,
        player_mana=6,
        player_board=[
            {"name": "随从A", "tags": {"ATK": 3, "HEALTH": 3, "EXHAUSTED": 0}},
            {"name": "随从B", "tags": {"ATK": 2, "HEALTH": 2, "EXHAUSTED": 0}},
        ],
        opponent_hp=0,
        opponent_armor=0,
        opponent_board=[],
    )

    # Already lethal
    assert state.is_lethal(), "Opponent at 0 HP should be lethal"

    # Engine should handle gracefully — may return lethal immediately or valid result
    engine = RHEAEngine(pop_size=20, max_gens=50, time_limit=150.0)
    result = engine.search(state)

    # No crash = success. Verify result is valid.
    assert result is not None, "Engine should return a result for dead-opponent state"
    assert result.best_chromosome, "Engine should return non-empty chromosome"
    # Fitness should be 10000 (lethal detected) or very high
    assert result.best_fitness >= 0, (
        f"Fitness should be non-negative for dead-opponent state, got {result.best_fitness}"
    )


